/**
 * ## Calculo do perímetro e área de um círculo
 */

console.log(`### Calculo da Area e Circunferencia do Circulo ###`);

// const piMath = Math.PI
const pi = 3.1415;
const raio = 1;

const perimetroCirculo = 2 * pi * raio;
const areaCirculo = pi * (raio ** 2);

console.log(`Para um círculo Raio = ${raio}; 
os valores do perimetro = ${perimetroCirculo} e Area = ${areaCirculo}`);